## These two functions provide functionality to compute
## the inverse of a matrix and cache it for later retrieval

## makeCacheMatrix creates a special matrix with  
## getters and setters for the original matrix itself 
## and it's inverse
makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinv <- function(solve) m <<- solve
    getinv <- function() m
    list(set = set, get = get,
         setinv = setinv,
         getinv = getinv)
}


## returns the inverse of a matrix either by computing
## it directly or retrieving it from a cache if
## previously computed
cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
    m <- x$getinv()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setinv(m)
    m
}
